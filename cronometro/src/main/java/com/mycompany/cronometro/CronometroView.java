    @Override
    public void receberAviso(long dado){
        //logica para atualizar o visor
        long minutos = dado /60000;
        long temp = dado % 60000;
        long segundos = temp/ 1000;
        long mili = temp % 1000;
        jVisor.setText(String.format("%02d:%02d.%03d", minutos, segundos, mili));
    }
