    public void addControladorMouse(ControlladorMouse c){
        controladorMouse = c;
        if(model != null){
            controladorMouse.addModel(model);
        }
    }
    
    public void addModel(CronometroModel m){
        model = m;
        if(controladorMouse != null){
            controladorMouse.addModel(model);
        }
    }

